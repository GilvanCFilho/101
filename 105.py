entrada = input('Quer "entrar" ou "sair" ?')

if entrada == 'entrar':
    print('Você entrou no sistema ')
elif entrada == 'sair':
    print('Você saiu do sistema')
else: 
    print('ERROR componente não indentificado') #ex de  